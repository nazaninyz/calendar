   <script>
  
       $(document).ready(function() {
           $(".abs").click(function(){
               var id = $(this).attr('id');
               if(id != 0) {       
                 $.ajax({
                   type: 'get',
                    url: 'showAbsence.php',
                   data: {id: id},
                   dataType: 'json',
                   async : false,
                   error: function(jqXHR, textStatus, errorThrown) {
                    if (jqXHR.status == 500) {
                       alert('Internal error: ' + jqXHR.responseText);
                   } else {
                       alert('Unexpected error.');
                     }
                   },
                   success: function(data){ 
                         $('#ill').empty();
                         $('#vac').empty();
                         $('#pers').empty();
                         var count=0;
                         var j=0; 
                         var k=0;
                         var z=0;
                        $.each(data,function(i,item) { 
                        
                    
            
						switch(data[i].kind)
            {

					   	case 'illness' :
                {          
                           j++;
                           $('#ill').append('<li class="list-group-item-danger">'+j+'.'+data[i].date+'</li>');
                           break;

                }

              case 'vacation' :
               {           
                           k++           
                           $('#vac').append('<li class="list-group-item-success">'+k+'.'+data[i].date+'</li>');
                           break;
               }
              case 'personal' :
               {                
                           z++;          
                           $('#pers').append('<li class="list-group-item-warning">'+z+'.'+data[i].date+'</li>');
                           break;
               }
            }					   
					   

             count=i+1;
					   })
                     
                     
                  $('#'+id+'p').text('number of absences in this month: '+count);

                     }
                
            })
            }
         }); 
     })


    </script> 



